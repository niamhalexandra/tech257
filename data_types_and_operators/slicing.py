hi_str = "Hello World!"
print(hi_str)

print(len(hi_str))

print("Get first character...")
print(hi_str[0])

print("Get last character...")
print(hi_str[-1])
print(hi_str[11])

print("Third character to 5th character...")
# formula for slicing hi_str[<starting index>:<ending index>])
print(hi_str[2:5])

print(hi_str[6:])

print("3rd last character to the end")
print(hi_str[-3:])

print(hi_str[:5])

